    @Override
    public void setupAnim(T entityIn, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
        this.lArm01.xRot = Mth.cos(limbSwing * 0.6662F) * 1.4F * limbSwingAmount + 0.18203784098300857F;
        this.rArm01.xRot = Mth.cos(limbSwing * 0.6662F + (float) Math.PI) * 1.4F * limbSwingAmount + 0.18203784098300857F;
        this.rLeg01.xRot = Mth.cos(limbSwing * 0.6662F) * 1.4F * limbSwingAmount - 0.136659280431156F;
        this.lLeg01.xRot = Mth.cos(limbSwing * 0.6662F + (float) Math.PI) * 1.4F * limbSwingAmount - 0.136659280431156F;

        if (!Minecraft.getInstance().isPaused()) {
            this.neck.xRot = getHeadPitch(entityIn) * 0.017453292F;
            this.neck.yRot = getHeadYaw(entityIn) * 0.017453292F;
        }

        float f = ageInTicks - (float) entityIn.tickCount;
        float f1 = entityIn.getStandingAnimationScale(f);
        if (f1 != 0) {
            float off = (float) Math.PI / 3F;
            this.hind.xRot = -off;
            this.hind.y = 3F;
            this.lLeg01.xRot = off - 0.136659280431156F;
            this.rLeg01.xRot = off - 0.136659280431156F;
            this.lArm01.xRot = Mth.sin(ageInTicks * 0.5F) * 0.9F + 0.18203784098300857F;
            this.rArm01.xRot = -Mth.sin(ageInTicks * 0.5F) * 0.9F + 0.18203784098300857F;
            if (!Minecraft.getInstance().isPaused()) {
                this.neck.xRot += off;
            }
        } else {
            this.hind.xRot = 0F;
            this.hind.y = 7.7F;
        }
    }

